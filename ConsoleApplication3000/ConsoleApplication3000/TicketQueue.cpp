#include "TicketQueue.h"
#include <iostream>
#include <iomanip> // Библиотека для красивого табличного вывода и форматирования чисел

// 1. Добавление новой заявки в очередь (в конец - tail)
void enqueue(TicketQueue& queue, const std::string& name, int priority, int h, int m, const std::string& issue) {
    Ticket t; // Создаем временную структуру заявки

    // Инициализируем заявку данными, передавая текущий свободный ID (queue.nextId) и увеличивая его на 1 (постфиксный ++)
    initTicket(t, queue.nextId++, name, priority, h, m, issue);

    // Создаем динамический узел в памяти с этой заявкой
    Node* newNode = createNode(t);

    // Проверяем состояние очереди: если хвоста нет, значит очередь была абсолютно пустой
    if (!queue.tail) {
        // В пустой очереди новый единственный элемент становится одновременно и головой, и хвостом
        queue.head = queue.tail = newNode;
    }
    else {
        // Если в очереди уже есть элементы:
        // Связываем текущий последний элемент (tail->next) с новым созданным узлом
        queue.tail->next = newNode;
        // Смещаем указатель tail на этот новый узел, делая его официальным концом очереди
        queue.tail = newNode;
    }
    std::cout << "Заявка №" << t.id << " успешно добавлена в очередь.\n";
}

// 2. Обработка (удаление) заявки оператором (из начала - head). Реализует принцип FIFO
bool dequeue(TicketQueue& queue) {
    // Если указатель head равен nullptr, значит обрабатывать нечего — очередь пуста
    if (!queue.head) {
        std::cout << "Очередь пуста. Нет заявок для обработки.\n";
        return false; // Возвращаем false как признак неудачи
    }

    // Запоминаем адрес текущей "головы" во временном указателе temp, чтобы не потерять адрес для удаления
    Node* temp = queue.head;

    // Выводим информацию о заявке, которую сейчас забирает оператор техподдержки
    std::cout << "\n>>> Оператор обработал заявку №" << temp->data.id << " <<<\n"
        << "Клиент: " << temp->data.clientName << "\n"
        << "Проблема: " << temp->data.issue << "\n";

    // Смещаем указатель "головы" на следующий элемент в очереди
    queue.head = queue.head->next;

    // Если после этого head стал равен nullptr, значит мы удалили последний элемент
    if (!queue.head) {
        queue.tail = nullptr; // Хвост тоже зануляем, очередь теперь полностью пуста
    }

    // Освобождаем динамическую память, которую занимал удаленный узел, через оператор delete
    delete temp;
    return true; // Возвращаем true — заявка успешно обработана и удалена
}

// 3. Просмотр следующей заявки без удаления
void peek(const TicketQueue& queue) {
    // Если head равен nullptr — смотреть нечего
    if (!queue.head) {
        std::cout << "Очередь пуста. Следующих заявок нет.\n";
        return;
    }
    // Выводим данные элемента, который стоит на очереди первым (находится на позиции head)
    std::cout << "\n--- Следующая заявка на очереди ---\n"
        << "ID: " << queue.head->data.id
        << " | Клиент: " << queue.head->data.clientName
        << " | Приоритет: " << queue.head->data.priority << "\n";
}

// 4. Вывод всех заявок в очереди в виде ровной текстовой таблицы
void displayAll(const TicketQueue& queue) {
    // Проверка на пустоту
    if (!queue.head) {
        std::cout << "Очередь заявок пуста.\n";
        return;
    }

    // Вывод красивой шапки таблицы. С помощью setw() задается фиксированная ширина колонок
    std::cout << "\n================СПИСОК ЗАЯВОК В ОЧЕРЕДИ================\n";
    std::cout << std::left << std::setw(6) << "ID"
        << std::setw(15) << "Имя"
        << std::setw(12) << "Приоритет"
        << std::setw(10) << "Время"
        << "Описание проблемы\n";
    std::cout << "-------------------------------------------------------\n";

    // Создаем указатель curr для итерации (обхода) списка, начинаем с head
    Node* curr = queue.head;

    // Цикл работает, пока curr указывает на существующий узел (не равен nullptr)
    while (curr) {
        // Преобразуем числовой приоритет в читаемый текст
        std::string prioStr = (curr->data.priority == 3) ? "Высокий" :
            (curr->data.priority == 2) ? "Средний" : "Низкий";

        // Выводим данные текущего узла. 
        // Комбинация setfill('0') и setw(2) превращает, например, 5 минут в строку "05" для корректного отображения времени
        std::cout << std::left << std::setw(6) << curr->data.id
            << std::setw(15) << curr->data.clientName
            << std::setw(12) << prioStr
            << std::setfill('0') << std::setw(2) << curr->data.time.hours << ":"
            << std::setfill('0') << std::setw(2) << curr->data.time.minutes << std::setfill(' ') << "     "
            << curr->data.issue << "\n";

        // Переходим к следующему элементу связанного списка
        curr = curr->next;
    }
    std::cout << "=======================================================\n";
}

// 5. Поиск заявки по номеру (ID)
void findById(const TicketQueue& queue, int id) {
    // Начинаем поиск с самого первого элемента (head)
    Node* curr = queue.head;

    // Последовательно обходим всю очередь узел за узлом
    while (curr) {
        // Если ID текущей заявки совпал с искомым — выводим её на экран
        if (curr->data.id == id) {
            std::cout << "\nЗаявка №" << id << " найдена:\n"
                << " Клиент: " << curr->data.clientName << "\n"
                << " Приоритет: " << curr->data.priority << "\n"
                << " Время: " << curr->data.time.hours << ":" << curr->data.time.minutes << "\n"
                << " Проблема: " << curr->data.issue << "\n";
            return; // Прерываем функцию, так как цель достигнута и ID уникальны
        }
        curr = curr->next; // Шаг вперед по списку
    }
    // Если цикл завершился, а функция не прервалась через return — элемент не найден
    std::cout << "Заявка с номером " << id << " не найдена в системе.\n";
}

// 6. Очистка очереди (вызывается в конце дня для предотвращения утечек памяти)
void clear(TicketQueue& queue) {
    // До тех пор, пока в голове очереди есть элементы
    while (queue.head) {
        Node* temp = queue.head;       // Запоминаем текущую голову
        queue.head = queue.head->next; // Смещаем голову вперед на следующий элемент
        delete temp;                   // Удаляем старую голову из памяти
    }
    // После удаления всех узлов обязательно обнуляем указатель на хвост
    queue.tail = nullptr;
    std::cout << "Очередь полностью очищена.\n";
}
