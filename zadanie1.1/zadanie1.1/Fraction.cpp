#include "Fraction.h"   // Заголовочный файл с объявлением класса Fraction
#include <stdexcept>    // Для использования std::invalid_argument (исключение при ошибке)
#include <cmath>        // Для использования std::abs (вычисление абсолютного значения)

// ===================================================
// ПРИВЕДЕНИЕ ДРОБИ К СОКРАЩЁННОМУ ВИДУ
// ===================================================
// Этот метод вызывается после каждого изменения числителя или знаменателя
// Выполняет две важные задачи:
//   1. Сокращает дробь (делит числитель и знаменатель на НОД)
//   2. Нормализует знак (знаменатель всегда положительный)
// @throws std::invalid_argument если знаменатель равен 0
void Fraction::reduce() {
    // Проверка на ноль знаменателя (защита от ошибок)
    if (denominator == 0) {
        throw std::invalid_argument("Знаменатель не может быть нулём");
    }

    // Находим наибольший общий делитель числителя и знаменателя
    int gcd = gcd_abs(numerator, denominator);

    // Делим числитель и знаменатель на НОД (сокращение)
    numerator /= gcd;
    denominator /= gcd;

    // Нормализация знака: знаменатель должен быть положительным
    // Если знаменатель отрицательный, меняем знак у числителя и знаменателя
    if (denominator < 0) {
        numerator = -numerator;     // Меняем знак числителя
        denominator = -denominator; // Делаем знаменатель положительным
    }
}

// ===================================================
// ВЫЧИСЛЕНИЕ НАИБОЛЬШЕГО ОБЩЕГО ДЕЛИТЕЛЯ (АЛГОРИТМ ЕВКЛИДА)
// ===================================================
// Алгоритм Евклида для нахождения НОД двух целых чисел
// @param a - первое число
// @param b - второе число
// @return наибольший общий делитель (всегда положительный)
// 
// Как работает алгоритм Евклида:
//   НОД(a, b) = НОД(b, a % b)
//   Повторяем пока b != 0
//   Пример: НОД(48, 18)
//     48 % 18 = 12
//     18 % 12 = 6
//     12 % 6 = 0
//     НОД = 6
int Fraction::gcd_abs(int a, int b) {
    // Берём абсолютные значения (НОД всегда положительный)
    a = std::abs(a);
    b = std::abs(b);

    // Алгоритм Евклида
    while (b != 0) {
        int temp = b;      // Сохраняем текущее значение b
        b = a % b;         // Новое b = остаток от деления a на b
        a = temp;          // Новое a = старое b
    }
    return a;  // Когда b = 0, a содержит НОД
}

// ===================================================
// КОНСТРУКТОР С ПАРАМЕТРАМИ
// ===================================================
// @param num - числитель дроби
// @param den - знаменатель дроби (не может быть 0)
// Инициализирует поля numerator и denominator через список инициализации
// Затем вызывает reduce() для сокращения и нормализации
// @throws std::invalid_argument если den == 0
Fraction::Fraction(int num, int den) : numerator(num), denominator(den) {
    // Проверка на ноль знаменателя
    if (den == 0) {
        throw std::invalid_argument("Знаменатель не может быть нулём");
    }
    // Приводим дробь к сокращённому виду
    reduce();
}

// ===================================================
// ОПЕРАТОР СЛОЖЕНИЯ (+)
// ===================================================
// Формула: a/b + c/d = (a*d + c*b) / (b*d)
// @param other - дробь, которую прибавляем
// @return новая дробь - результат сложения
// 
// Пример: 1/2 + 3/4 = (1*4 + 3*2) / (2*4) = (4+6)/8 = 10/8 = 5/4
Fraction Fraction::operator+(const Fraction& other) const {
    return Fraction(
        numerator * other.denominator + other.numerator * denominator,  // Новый числитель
        denominator * other.denominator                                 // Новый знаменатель
    );
}

// ===================================================
// ОПЕРАТОР ВЫЧИТАНИЯ (-)
// ===================================================
// Формула: a/b - c/d = (a*d - c*b) / (b*d)
// @param other - дробь, которую вычитаем
// @return новая дробь - разность
//
// Пример: 3/4 - 1/2 = (3*2 - 1*4) / (4*2) = (6-4)/8 = 2/8 = 1/4
Fraction Fraction::operator-(const Fraction& other) const {
    return Fraction(
        numerator * other.denominator - other.numerator * denominator,  // Новый числитель
        denominator * other.denominator                                 // Новый знаменатель
    );
}

// ===================================================
// ОПЕРАТОР УМНОЖЕНИЯ (*)
// ===================================================
// Формула: a/b * c/d = (a*c) / (b*d)
// @param other - дробь, на которую умножаем
// @return новая дробь - произведение
//
// Пример: 1/2 * 3/4 = (1*3) / (2*4) = 3/8
Fraction Fraction::operator*(const Fraction& other) const {
    return Fraction(
        numerator * other.numerator,      // Числитель = произведение числителей
        denominator * other.denominator   // Знаменатель = произведение знаменателей
    );
}

// ===================================================
// ОПЕРАТОР ДЕЛЕНИЯ (/)
// ===================================================
// Формула: a/b / c/d = (a*d) / (b*c)
// @param other - делитель (дробь, на которую делим)
// @return новая дробь - частное
// @throws std::invalid_argument если other.numerator == 0 (деление на ноль)
//
// Пример: 1/2 / 3/4 = (1*4) / (2*3) = 4/6 = 2/3
Fraction Fraction::operator/(const Fraction& other) const {
    // Проверка деления на ноль
    if (other.numerator == 0) {
        throw std::invalid_argument("Деление на ноль");
    }
    return Fraction(
        numerator * other.denominator,   // Числитель = a * d
        denominator * other.numerator    // Знаменатель = b * c
    );
}

// ===================================================
// ОПЕРАТОР РАВЕНСТВА (==)
// ===================================================
// Две дроби равны, если у них одинаковые числитель и знаменатель
// (после сокращения, которое гарантирует класс)
// @param other - дробь для сравнения
// @return true - если дроби равны, false - иначе
bool Fraction::operator==(const Fraction& other) const {
    return numerator == other.numerator && denominator == other.denominator;
}

// ===================================================
// ОПЕРАТОР НЕРАВЕНСТВА (!=)
// ===================================================
// @return true - если дроби не равны, false - иначе
// Реализуется через отрицание operator==
bool Fraction::operator!=(const Fraction& other) const {
    return !(*this == other);  // Просто инвертируем результат operator==
}

// ===================================================
// ОПЕРАТОР МЕНЬШЕ (<)
// ===================================================
// Сравнение через перекрёстное умножение (избегаем деления)
// Формула: a/b < c/d  ⇔  a*d < c*b
// @param other - дробь для сравнения
// @return true - если текущая дробь меньше other, false - иначе
//
// Пример: 1/2 < 3/4 ?  1*4 < 3*2 ? 4 < 6 ? Да
bool Fraction::operator<(const Fraction& other) const {
    return numerator * other.denominator < other.numerator * denominator;
}

// ===================================================
// ОПЕРАТОР БОЛЬШЕ (>)
// ===================================================
// @return true - если текущая дробь больше other, false - иначе
// Используем тот факт, что a > b эквивалентно b < a
bool Fraction::operator>(const Fraction& other) const {
    return other < *this;  // Переиспользуем operator<
}

// ===================================================
// ОПЕРАТОР ВЫВОДА (<<) - ДРУЖЕСТВЕННАЯ ФУНКЦИЯ
// ===================================================
// Позволяет выводить дробь в поток: cout << f;
// @param os - поток вывода (cout, файл и т.д.)
// @param f - выводимая дробь
// @return ссылка на поток (для цепочечного вывода)
//
// Форматы вывода:
//   - Если знаменатель = 1: выводит только числитель (целое число)
//   - Иначе: выводит "числитель/знаменатель"
std::ostream& operator<<(std::ostream& os, const Fraction& f) {
    if (f.denominator == 1) {
        // Дробь является целым числом: выводим только числитель
        os << f.numerator;
    }
    else {
        // Обыкновенная дробь: выводим "числитель/знаменатель"
        os << f.numerator << "/" << f.denominator;
    }
    return os;  // Возвращаем поток для цепочечных операций
}

// ===================================================
// ОПЕРАТОР ВВОДА (>>) - ДРУЖЕСТВЕННАЯ ФУНКЦИЯ
// ===================================================
// Позволяет вводить дробь из потока: cin >> f;
// @param is - поток ввода (cin, файл и т.д.)
// @param f - дробь для заполнения
// @return ссылка на поток
//
// Поддерживаемые форматы ввода:
//   - "a/b" - обыкновенная дробь (например "3/4")
//   - "a"   - целое число (например "5" будет интерпретировано как 5/1)
std::istream& operator>>(std::istream& is, Fraction& f) {
    char slash;        // Для хранения символа '/' (если есть)
    int num, den = 1;  // Числитель и знаменатель (знаменатель по умолчанию 1)

    // Сначала считываем число (числитель)
    is >> num;

    // Проверяем, есть ли в потоке символ '/'
    // peek() смотрит на следующий символ, не извлекая его
    if (is.peek() == '/') {
        // Если есть '/', считываем его и знаменатель
        is >> slash >> den;
    }

    // Создаём дробь из считанных чисел (конструктор сам сократит)
    f = Fraction(num, den);

    return is;  // Возвращаем поток для цепочечных операций
}