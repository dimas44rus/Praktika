// ===================================================
// ПОДКЛЮЧЕНИЕ ЗАГОЛОВОЧНЫХ ФАЙЛОВ
// ===================================================
#include "Vector3D.h"   // Заголовочный файл с объявлением класса Vector3D
#include <stdexcept>    // Для использования std::out_of_range (исключение при выходе за границы)

// ===================================================
// ИНИЦИАЛИЗАЦИЯ СТАТИЧЕСКОГО ПОЛЯ
// ===================================================
// Статические поля класса должны быть определены (инициализированы) в .cpp файле
// count - счётчик количества существующих объектов Vector3D
// Начальное значение = 0 (ещё не создано ни одного вектора)
int Vector3D::count = 0;

// ===================================================
// КОНСТРУКТОР С ПАРАМЕТРАМИ
// ===================================================
// Создаёт новый вектор с заданными координатами
// @param x, y, z - координаты вектора (по умолчанию 0 в заголовке)
Vector3D::Vector3D(double x, double y, double z) {
    // Заполняем массив координат
    coords[0] = x;   // X координата
    coords[1] = y;   // Y координата
    coords[2] = z;   // Z координата
    count++;         // Увеличиваем счётчик объектов на 1
}

// ===================================================
// КОНСТРУКТОР КОПИРОВАНИЯ
// ===================================================
// Создаёт новый вектор как копию существующего
// @param other - ссылка на копируемый вектор (константная, чтобы не изменять оригинал)
Vector3D::Vector3D(const Vector3D& other) {
    // Копируем все три координаты из другого вектора
    for (int i = 0; i < 3; i++) {
        coords[i] = other.coords[i];   // Копируем координату
    }
    count++;         // Увеличиваем счётчик (создан новый объект)
}

// ===================================================
// ДЕСТРУКТОР
// ===================================================
// Вызывается автоматически при уничтожении объекта
// Уменьшает счётчик существующих векторов
Vector3D::~Vector3D() {
    count--;         // Уменьшаем счётчик объектов на 1
}

// ===================================================
// СТАТИЧЕСКИЙ МЕТОД - ПОЛУЧЕНИЕ КОЛИЧЕСТВА ВЕКТОРОВ
// ===================================================
// @return текущее количество существующих объектов Vector3D
int Vector3D::getCount() {
    return count;    // Возвращаем значение статического поля
}

// ===================================================
// ОПЕРАТОР СЛОЖЕНИЯ ВЕКТОРОВ
// ===================================================
// Выполняет векторное сложение: (x1+x2, y1+y2, z1+z2)
// @param other - вектор, который прибавляем
// @return новый вектор - результат сложения
// Пример: v3 = v1 + v2;
Vector3D Vector3D::operator+(const Vector3D& other) const {
    // Создаём и возвращаем новый вектор с суммой координат
    return Vector3D(
        coords[0] + other.coords[0],   // X1 + X2
        coords[1] + other.coords[1],   // Y1 + Y2
        coords[2] + other.coords[2]    // Z1 + Z2
    );
}

// ===================================================
// ОПЕРАТОР ВЫЧИТАНИЯ ВЕКТОРОВ
// ===================================================
// Выполняет векторное вычитание: (x1-x2, y1-y2, z1-z2)
// @param other - вектор, который вычитаем
// @return новый вектор - разность
Vector3D Vector3D::operator-(const Vector3D& other) const {
    return Vector3D(
        coords[0] - other.coords[0],   // X1 - X2
        coords[1] - other.coords[1],   // Y1 - Y2
        coords[2] - other.coords[2]    // Z1 - Z2
    );
}

// ===================================================
// ОПЕРАТОР СКАЛЯРНОГО ПРОИЗВЕДЕНИЯ
// ===================================================
// Скалярное произведение: x1*x2 + y1*y2 + z1*z2
// @param other - второй вектор
// @return число (скаляр) - результат скалярного произведения
// Геометрический смысл: длина проекции одного вектора на другой
// Пример: v1(1,2,3) * v2(4,5,6) = 1*4 + 2*5 + 3*6 = 4 + 10 + 18 = 32
double Vector3D::operator*(const Vector3D& other) const {
    return coords[0] * other.coords[0] +   // X1 * X2
        coords[1] * other.coords[1] +   // Y1 * Y2
        coords[2] * other.coords[2];    // Z1 * Z2
}

// ===================================================
// ОПЕРАТОР УМНОЖЕНИЯ ВЕКТОРА НА СКАЛЯР
// ===================================================
// Умножает каждую координату на число: (x*s, y*s, z*s)
// @param scalar - число (скаляр)
// @return новый вектор - результат умножения
Vector3D Vector3D::operator*(double scalar) const {
    return Vector3D(
        coords[0] * scalar,   // X * scalar
        coords[1] * scalar,   // Y * scalar
        coords[2] * scalar    // Z * scalar
    );
}

// ===================================================
// ДРУЖЕСТВЕННАЯ ФУНКЦИЯ: УМНОЖЕНИЕ СКАЛЯРА НА ВЕКТОР
// ===================================================
// Позволяет писать: 3 * v1 (скаляр слева)
// @param scalar - число
// @param v - вектор
// @return новый вектор - результат умножения
// Использует уже определённый operator* (скаляр справа)
Vector3D operator*(double scalar, const Vector3D& v) {
    return v * scalar;   // Просто вызываем v * scalar
}

// ===================================================
// ПРЕФИКСНЫЙ ИНКРЕМЕНТ (++v)
// ===================================================
// Увеличивает все координаты на 1, затем возвращает изменённый вектор
// @return ссылка на текущий (уже изменённый) объект
// Пример: ++v;  // все координаты увеличились на 1
Vector3D& Vector3D::operator++() {
    // Увеличиваем каждую координату на 1
    for (int i = 0; i < 3; i++) {
        coords[i]++;
    }
    return *this;   // Возвращаем ссылку на текущий объект (уже изменённый)
}

// ===================================================
// ПОСТФИКСНЫЙ ИНКРЕМЕНТ (v++)
// ===================================================
// Возвращает старую копию вектора, затем увеличивает координаты на 1
// @return копия вектора ДО изменения
// Фиктивный параметр int нужен для отличия от префиксного инкремента
Vector3D Vector3D::operator++(int) {
    Vector3D temp = *this;   // Сохраняем копию текущего вектора (старое значение)
    ++(*this);               // Вызываем префиксный инкремент (увеличиваем текущий)
    return temp;             // Возвращаем старую копию
}

// ===================================================
// ОПЕРАТОР ДОСТУПА ПО ИНДЕКСУ (НЕКОНСТАНТНЫЙ)
// ===================================================
// Позволяет обращаться к координатам через квадратные скобки: v[0], v[1], v[2]
// Можно изменять значение: v[0] = 10;
// @param index - индекс координаты (0, 1, 2)
// @return ссылка на координату (позволяет изменять значение)
// @throws std::out_of_range при неверном индексе
double& Vector3D::operator[](int index) {
    // Проверяем корректность индекса
    if (index < 0 || index >= 3) {
        throw std::out_of_range("Индекс вне диапазона");  // Выбрасываем исключение
    }
    return coords[index];   // Возвращаем ссылку на координату
}

// ===================================================
// ОПЕРАТОР ДОСТУПА ПО ИНДЕКСУ (КОНСТАНТНЫЙ)
// ===================================================
// Для константных объектов (только для чтения)
// @return константная ссылка (нельзя изменить значение)
// Метод помечен const, значит не изменяет объект
const double& Vector3D::operator[](int index) const {
    if (index < 0 || index >= 3) {
        throw std::out_of_range("Индекс вне диапазона");
    }
    return coords[index];   // Возвращаем константную ссылку
}

// ===================================================
// ОПЕРАТОР += (СЛОЖЕНИЕ С ПРИСВАИВАНИЕМ)
// ===================================================
// Прибавляет другой вектор к текущему
// @param other - вектор, который прибавляем
// @return ссылка на текущий (изменённый) объект
// Пример: v1 += v2;  // v1 = v1 + v2;
Vector3D& Vector3D::operator+=(const Vector3D& other) {
    // Прибавляем каждую координату другого вектора
    for (int i = 0; i < 3; i++) {
        coords[i] += other.coords[i];
    }
    return *this;   // Возвращаем ссылку на текущий объект
}

// ===================================================
// ОПЕРАТОР -= (ВЫЧИТАНИЕ С ПРИСВАИВАНИЕМ)
// ===================================================
// Вычитает другой вектор из текущего
// @param other - вектор, который вычитаем
// @return ссылка на текущий (изменённый) объект
Vector3D& Vector3D::operator-=(const Vector3D& other) {
    for (int i = 0; i < 3; i++) {
        coords[i] -= other.coords[i];
    }
    return *this;
}

// ===================================================
// ОПЕРАТОР *= (УМНОЖЕНИЕ НА СКАЛЯР С ПРИСВАИВАНИЕМ)
// ===================================================
// Умножает текущий вектор на скаляр
// @param scalar - число (скаляр)
// @return ссылка на текущий (изменённый) объект
Vector3D& Vector3D::operator*=(double scalar) {
    for (int i = 0; i < 3; i++) {
        coords[i] *= scalar;
    }
    return *this;
}

// ===================================================
// ОПЕРАТОР ВЫВОДА (ДРУЖЕСТВЕННАЯ ФУНКЦИЯ)
// ===================================================
// Позволяет выводить вектор в поток: cout << v;
// Формат вывода: (x, y, z)
// @param os - поток вывода (cout, файл и т.д.)
// @param v - выводимый вектор
// @return ссылка на поток (для цепочечного вывода)
std::ostream& operator<<(std::ostream& os, const Vector3D& v) {
    // Используем const-версию оператора [] для доступа к координатам
    os << "(" << v[0] << ", " << v[1] << ", " << v[2] << ")";
    return os;   // Возвращаем поток для цепочечных операций (cout << v1 << v2;)
}