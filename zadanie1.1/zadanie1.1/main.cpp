/**
 * Демонстрация работы класса Fraction:
 * - Создание дробей
 * - Арифметические операции (+, -, *, /)
 * - Операции сравнения (==, <)
 * - Обработка ошибок через try-catch
 */
void demoFraction() {
    printHeader("Задание 1.1: Класс Fraction (Дроби)");

    try {   // try-блок для перехвата исключений (например, деление на ноль)

        // Создаём объекты дробей: 1/2 и 3/4
        // Конструктор автоматически сокращает дробь
        Fraction a(1, 2);   // a = 1/2
        Fraction b(3, 4);   // b = 3/4

        // Выводим исходные дроби
        cout << "a = " << a << endl;
        cout << "b = " << b << endl;

        // Демонстрация перегруженных арифметических операторов
        cout << "a + b = " << a + b << endl;  // operator+
        cout << "a - b = " << a - b << endl;  // operator-
        cout << "a * b = " << a * b << endl;  // operator*
        cout << "a / b = " << a / b << endl;  // operator/

        // Демонстрация перегруженных операторов сравнения
        // Тернарный оператор ?: возвращает "Да" если true, "Нет" если false
        cout << "a == b: " << (a == b ? "Да" : "Нет") << endl;  // operator==
        cout << "a < b: " << (a < b ? "Да" : "Нет") << endl;    // operator<

    }
    catch (const exception& e) {  // Ловим любые исключения
        cerr << "Ошибка: " << e.what() << endl;  // Выводим сообщение об ошибке
    }
}

