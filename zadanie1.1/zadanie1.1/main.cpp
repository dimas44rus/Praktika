/**
 * Демонстрация работы класса Vector3D:
 * - Создание векторов
 * - Векторная сумма и разность (+ и -)
 * - Скалярное произведение (*)
 * - Умножение на скаляр
 * - Префиксный и постфиксный инкремент (++)
 * - Доступ к координатам через оператор []
 * - Статический метод подсчёта объектов
 */
void demoVector3D() {
    printHeader("Задание 1.2: Класс Vector3D (Векторы)");

    try {
        // Создаём два трёхмерных вектора
        Vector3D v1(1, 2, 3);   // Вектор (1, 2, 3)
        Vector3D v2(4, 5, 6);   // Вектор (4, 5, 6)

        // Выводим векторы
        cout << "v1 = " << v1 << endl;
        cout << "v2 = " << v2 << endl;

        // Векторная сумма и разность (поэлементное сложение/вычитание)
        cout << "v1 + v2 = " << v1 + v2 << endl;  // operator+
        cout << "v1 - v2 = " << v1 - v2 << endl;  // operator-

        // Скалярное произведение: (1*4 + 2*5 + 3*6) = 4 + 10 + 18 = 32
        cout << "v1 * v2 = " << v1 * v2 << endl;  // operator* (скалярное)

        // Умножение вектора на скаляр (каждая координата умножается на число)
        cout << "v1 * 3 = " << v1 * 3 << endl;    // operator* (число)
        cout << "2 * v1 = " << 2 * v1 << endl;    // дружественная функция operator*

        // Демонстрация инкремента
        Vector3D v3(1, 1, 1);   // Создаём вектор (1, 1, 1)
        cout << "\nv3 = " << v3 << endl;

        ++v3;  // Префиксный инкремент: сначала увеличивает, потом возвращает
        cout << "++v3 = " << v3 << endl;  // Стало (2, 2, 2)
        // Постфиксный инкремент не показан, но он тоже перегружен

        // Доступ к координатам через оператор []
        cout << "\nv3[0] = " << v3[0] << endl;   // Получаем X-координату
        v3[1] = 10;                               // Изменяем Y-координату
        cout << "После v3[1]=10: " << v3 << endl; // Стало (2, 10, 2)

        // Статический метод: показывает сколько всего векторов создано
        // (с учётом конструкторов копирования и деструкторов)
        cout << "\nВсего векторов: " << Vector3D::getCount() << endl;

    }
    catch (const exception& e) {
        cerr << "Ошибка: " << e.what() << endl;
    }
}

