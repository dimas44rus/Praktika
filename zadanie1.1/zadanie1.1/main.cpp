/**
 * Демонстрация работы шаблонного класса Matrix:
 * - Создание матриц заданного размера
 * - Доступ к элементам через operator()
 * - Сложение, вычитание, умножение матриц
 * - Умножение на скаляр
 * - Транспонирование (!)
 * - Правило трёх (конструктор копирования, оператор=, деструктор)
 */
void demoMatrix() {
    printHeader("Задание 1.3: Класс Matrix (Матрицы)");

    try {
        // Создаём две матрицы 2x2 из целых чисел
        // Шаблон <int> означает, что матрица будет хранить int
        Matrix<int> m1(2, 2);
        Matrix<int> m2(2, 2);

        // Заполняем первую матрицу значениями
        // operator() позволяет обращаться к элементам как m1(строка, столбец)
        m1(0, 0) = 1; m1(0, 1) = 2;  // Первая строка: [1, 2]
        m1(1, 0) = 3; m1(1, 1) = 4;  // Вторая строка: [3, 4]

        // Заполняем вторую матрицу
        m2(0, 0) = 5; m2(0, 1) = 6;  // [5, 6]
        m2(1, 0) = 7; m2(1, 1) = 8;  // [7, 8]

        // Выводим матрицы (используется перегруженный operator<<)
        cout << "Матрица 1:\n" << m1;
        cout << "Матрица 2:\n" << m2;

        // Поэлементное сложение матриц
        cout << "\nСумма:\n" << m1 + m2;      // operator+

        // Поэлементное вычитание матриц
        cout << "Разность:\n" << m1 - m2;      // operator-

        // Матричное умножение (не поэлементное!)
        // Результат: матрица 2x2, где каждый элемент = сумма произведений
        cout << "Произведение:\n" << m1 * m2;  // operator*

        // Умножение всех элементов матрицы на скаляр 3
        cout << "Умножение на 3:\n" << m1 * 3; // operator* (скаляр)

        // Транспонирование: меняем строки и столбцы местами
        // ! - унарный оператор, перегружен для транспонирования
        cout << "Транспонирование:\n" << !m1;   // operator!

        // Доступ к отдельному элементу
        cout << "\nm1(0,0) = " << m1(0, 0) << endl;

        // Изменение элемента
        m1(0, 0) = 100;
        cout << "После m1(0,0)=100:\n" << m1;

    }
    catch (const exception& e) {
        cerr << "Ошибка: " << e.what() << endl;
    }
}

