#ifndef VECTOR3D_H
#define VECTOR3D_H

// ===================================================
// ПОДКЛЮЧЕНИЕ БИБЛИОТЕК
// ===================================================
#include <iostream>  // Для использования std::ostream (оператор вывода)

// ===================================================
// КЛАСС VECTOR3D - ДЛЯ РАБОТЫ С ТРЁХМЕРНЫМИ ВЕКТОРАМИ
// ===================================================
// Представляет вектор в трёхмерном пространстве с координатами (x, y, z)
class Vector3D {

    // ===================================================
    // ПРИВАТНЫЕ ПОЛЯ (СКРЫТЫ ОТ ПОЛЬЗОВАТЕЛЯ)
    // ===================================================
private:
    double coords[3];   // Массив для хранения трёх координат: [0]=x, [1]=y, [2]=z
    static int count;   // СТАТИЧЕСКОЕ ПОЛЕ - счётчик всех созданных объектов
    // static означает, что поле принадлежит классу, а не объекту
    // Используется для подсчёта количества существующих векторов

// ===================================================
// ПУБЛИЧНЫЕ МЕТОДЫ (ДОСТУПНЫЕ ПОЛЬЗОВАТЕЛЮ)
// ===================================================
public:

    // ===================================================
    // КОНСТРУКТОР С ПАРАМЕТРАМИ ПО УМОЛЧАНИЮ
    // ===================================================
    // @param x - координата X (по умолчанию 0)
    // @param y - координата Y (по умолчанию 0)
    // @param z - координата Z (по умолчанию 0)
    // Примеры создания:
    //   Vector3D v1;           // (0, 0, 0)
    //   Vector3D v2(1, 2, 3);  // (1, 2, 3)
    //   Vector3D v3(5, 6);     // (5, 6, 0) - третья координата по умолчанию
    Vector3D(double x = 0, double y = 0, double z = 0);

    // ===================================================
    // КОНСТРУКТОР КОПИРОВАНИЯ
    // ===================================================
    // Создаёт новый вектор как копию существующего
    // @param other - ссылка на копируемый вектор
    // Пример: Vector3D v2(v1);
    Vector3D(const Vector3D& other);

    // ===================================================
    // ДЕСТРУКТОР
    // ===================================================
    // Вызывается при уничтожении объекта
    // Уменьшает счётчик количества векторов на 1
    ~Vector3D();

    // ===================================================
    // СТАТИЧЕСКИЙ МЕТОД - ПОЛУЧЕНИЕ КОЛИЧЕСТВА ВЕКТОРОВ
    // ===================================================
    // static метод можно вызывать без создания объекта: Vector3D::getCount()
    // @return количество существующих в данный момент объектов Vector3D
    // Используется для отладки и контроля утечек памяти
    static int getCount();

    // ===================================================
    // ОПЕРАТОР СЛОЖЕНИЯ ВЕКТОРОВ
    // ===================================================
    // Выполняет векторное сложение: (x1+x2, y1+y2, z1+z2)
    // @param other - вектор, который прибавляем
    // @return новый вектор - результат сложения
    // Пример: v3 = v1 + v2;
    Vector3D operator+(const Vector3D& other) const;

    // ===================================================
    // ОПЕРАТОР ВЫЧИТАНИЯ ВЕКТОРОВ
    // ===================================================
    // Выполняет векторное вычитание: (x1-x2, y1-y2, z1-z2)
    // @param other - вектор, который вычитаем
    // @return новый вектор - разность
    Vector3D operator-(const Vector3D& other) const;

    // ===================================================
    // ОПЕРАТОР СКАЛЯРНОГО ПРОИЗВЕДЕНИЯ
    // ===================================================
    // Скалярное произведение: x1*x2 + y1*y2 + z1*z2
    // Результат - число (скаляр), не вектор!
    // @param other - второй вектор
    // @return число - скалярное произведение
    // Пример: double dot = v1 * v2;  // результат 1*4 + 2*5 + 3*6 = 32
    double operator*(const Vector3D& other) const;

    // ===================================================
    // ОПЕРАТОР УМНОЖЕНИЯ ВЕКТОРА НА СКАЛЯР
    // ===================================================
    // Умножает каждую координату на число: (x*s, y*s, z*s)
    // @param scalar - число (скаляр)
    // @return новый вектор - результат умножения
    // Пример: v2 = v1 * 3;  // (3, 6, 9)
    Vector3D operator*(double scalar) const;

    // ===================================================
    // ДРУЖЕСТВЕННАЯ ФУНКЦИЯ: УМНОЖЕНИЕ СКАЛЯРА НА ВЕКТОР
    // ===================================================
    // Позволяет писать: 3 * v1 (скаляр слева)
    // Дружественная функция имеет доступ к приватным полям класса
    // @param scalar - число
    // @param v - вектор
    // @return новый вектор - результат умножения
    friend Vector3D operator*(double scalar, const Vector3D& v);

    // ===================================================
    // ПРЕФИКСНЫЙ ИНКРЕМЕНТ (++v)
    // ===================================================
    // Увеличивает все координаты на 1, затем возвращает изменённый вектор
    // @return ссылка на текущий (уже изменённый) объект
    // Пример: ++v;  // все координаты увеличились на 1
    Vector3D& operator++();

    // ===================================================
    // ПОСТФИКСНЫЙ ИНКРЕМЕНТ (v++)
    // ===================================================
    // Возвращает старую копию вектора, затем увеличивает все координаты на 1
    // @return копия вектора ДО изменения
    // Пример: Vector3D old = v++;  // old - старый вектор, v - увеличенный
    // Фиктивный параметр int нужен для отличия от префиксного инкремента
    Vector3D operator++(int);

    // ===================================================
    // ОПЕРАТОР ДОСТУПА ПО ИНДЕКСУ (НЕКОНСТАНТНЫЙ)
    // ===================================================
    // Позволяет обращаться к координатам через квадратные скобки: v[0], v[1], v[2]
    // Можно изменять значение: v[0] = 10;
    // @param index - индекс координаты (0, 1, 2)
    // @return ссылка на координату (позволяет изменять значение)
    // @throws out_of_range при неверном индексе
    double& operator[](int index);

    // ===================================================
    // ОПЕРАТОР ДОСТУПА ПО ИНДЕКСУ (КОНСТАНТНЫЙ)
    // ===================================================
    // Для константных объектов (только для чтения)
    // @return константная ссылка (нельзя изменить)
    const double& operator[](int index) const;

    // ===================================================
    // СОСТАВНЫЕ ОПЕРАТОРЫ (С ПРИСВАИВАНИЕМ)
    // ===================================================

    // Оператор +=: прибавляет другой вектор к текущему
    // Пример: v1 += v2;  // v1 = v1 + v2;
    Vector3D& operator+=(const Vector3D& other);

    // Оператор -=: вычитает другой вектор из текущего
    // Пример: v1 -= v2;  // v1 = v1 - v2;
    Vector3D& operator-=(const Vector3D& other);

    // Оператор *=: умножает текущий вектор на скаляр
    // Пример: v1 *= 3;   // v1 = v1 * 3;
    Vector3D& operator*=(double scalar);

    // ===================================================
    // ОПЕРАТОР ВЫВОДА (ДРУЖЕСТВЕННАЯ ФУНКЦИЯ)
    // ===================================================
    // Позволяет выводить вектор в поток: cout << v;
    // Формат вывода: (x, y, z)
    // @param os - поток вывода (cout, файл и т.д.)
    // @param v - выводимый вектор
    // @return ссылка на поток (для цепочечного вывода)
    friend std::ostream& operator<<(std::ostream& os, const Vector3D& v);
};

// ===================================================
// КОНЕЦ ЗАЩИТЫ ОТ ПОВТОРНОГО ВКЛЮЧЕНИЯ
// ===================================================
#endif