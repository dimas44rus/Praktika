#ifndef PHONEBOOK_H
#define PHONEBOOK_H

#include <iostream>
#include <vector>

/**
 * Класс Телефонная книга
 * Позволяет хранить, добавлять, удалять и искать контакты
 *
 * Требования из задания:
 * - Динамическая память под ФИО
 * - Конструкторы, деструктор
 * - Inline-функции (геттеры)
 * - Инициализаторы в конструкторах
 */
class PhoneBook {
private:
    /**
     * Внутренняя структура для хранения данных об одном контакте
     * Инкапсулирована внутри класса PhoneBook
     */
    struct Contact {
        char* fullName;          // ФИО (динамическая память!)
        char homePhone[20];      // Домашний телефон (фиксированный массив)
        char workPhone[20];      // Рабочий телефон
        char mobilePhone[20];    // Мобильный телефон
        char additionalInfo[200]; // Дополнительная информация о контакте

        // === Конструкторы и деструктор для Contact ===

        /**
         * Конструктор по умолчанию для Contact
         * Инициализирует все поля нулями/пустыми строками
         * fullName = nullptr означает, что память ещё не выделена
         */
        Contact();

        /**
         * Деструктор для Contact
         * Освобождает динамически выделенную память под fullName
         * Это важно для предотвращения утечек памяти!
         */
        ~Contact();

        /**
         * Конструктор копирования для Contact
         * Выполняет ГЛУБОКОЕ копирование (создаёт новую строку в памяти)
         * Без него при копировании будет копироваться только указатель
         */
        Contact(const Contact& other);

        /**
         * Оператор присваивания для Contact
         * Также выполняет глубокое копирование
         * Защищает от самоприсваивания (проверка this != &other)
         */
        Contact& operator=(const Contact& other);
    };

    std::vector<Contact> contacts;  // Вектор для хранения всех контактов
    // vector автоматически управляет массивом контактов
    // но Конструктор копирования Contact нужен для глубокого копирования строк!

    // === Вспомогательные приватные методы ===

    /**
     * Создаёт строку ФИО из трёх частей
     * @param surname фамилия
     * @param name имя
     * @param patronymic отчество
     * @return указатель на новую строку в динамической памяти
     *
     * Внимание: вызывающий обязан удалить эту строку через delete[]
     */
    char* createFullName(const char* surname, const char* name, const char* patronymic);

    /**
     * Удобная функция для ввода строки с подсказкой
     * @param buffer куда сохранить строку
     * @param size размер буфера
     * @param prompt текст подсказки
     */
    void inputString(char* buffer, int size, const char* prompt);

public:
    // === Публичные методы (интерфейс класса) ===

    /**
     * Конструктор по умолчанию для PhoneBook
     * Использует список инициализации (contacts инициализируется автоматически)
     * Телефонная книга создаётся пустой
     */
    PhoneBook();

    /**
     * Деструктор для PhoneBook
     * Очищает вектор, что автоматически вызывает деструкторы всех Contact
     * А те, в свою очередь, освобождают память под ФИО
     */
    ~PhoneBook();

    // === Основные функции по заданию ===

    /**
     * Добавление нового абонента
     * Запрашивает у пользователя все данные
     * Создаёт новый Contact и добавляет в вектор
     * Память под ФИО выделяется динамически
     */
    void addContact();

    /**
     * Удаление абонента
     * Запрашивает ФИО для удаления
     * Ищет контакт с таким ФИО и удаляет его из вектора
     */
    void removeContact();

    /**
     * Поиск абонента по ФИО
     * Запрашивает ФИО для поиска
     * Выводит всю информацию о найденном контакте
     */
    void searchByName();

    /**
     * Вывод всех абонентов на экран
     * Проходит по всем контактам в векторе и выводит их
     */
    void showAllContacts();

    /**
     * Главное меню программы
     * Реализует цикл выбора действий пользователем
     * Завершается при выборе пункта "Выход"
     */
    void menu();

    // === Дополнительные полезные методы ===

    /**
     * Возвращает количество контактов
     * Inline-функция (определена прямо в заголовочном файле)
     */
    int getContactCount() const { return contacts.size(); }

    /**
     * Очищает всю телефонную книгу
     * Удаляет все контакты из вектора
     */
    void clearAll();
};

#endif // PHONEBOOK_H