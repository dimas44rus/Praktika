#include <iostream>     // Для ввода/вывода (cout, cin)
#include <cmath>        // Для математических функций (sqrt)
#include <stdexcept>    // Для классов исключений (invalid_argument)

using namespace std;    // Чтобы не писать std:: перед cout и cin

// ===================================================
// АБСТРАКТНЫЙ БАЗОВЫЙ КЛАСС SHAPE (ФИГУРА)
// ===================================================
// Абстрактный класс - нельзя создать объект этого типа
// Он служит основой (интерфейсом) для всех геометрических фигур
class Shape {
protected:
    // ===================================================
    // СТАТИЧЕСКОЕ ПОЛЕ ДЛЯ ПОДСЧЁТА ФИГУР
    // ===================================================
    // static означает, что поле принадлежит классу, а не объекту
    // Одно общее поле для всех фигур, а не отдельное для каждой
    // Используется для подсчёта количества существующих фигур
    static int shapeCount;

public:
    // ===================================================
    // КОНСТРУКТОР
    // ===================================================
    // Увеличивает счётчик фигур при создании нового объекта
    Shape() {
        shapeCount++;   // При создании фигуры увеличиваем счётчик
    }

    // ===================================================
    // ВИРТУАЛЬНЫЙ ДЕСТРУКТОР
    // ===================================================
    // virtual ОБЯЗАТЕЛЕН для полиморфного удаления объектов
    // При delete shape_ptr будет вызван правильный деструктор производного класса
    // Уменьшает счётчик фигур при удалении объекта
    virtual ~Shape() {
        shapeCount--;   // При удалении фигуры уменьшаем счётчик
    }

    // ===================================================
    // ЧИСТО ВИРТУАЛЬНЫЕ МЕТОДЫ (АБСТРАКТНЫЙ КЛАСС)
    // ===================================================
    // = 0 означает, что метод не имеет реализации в базовом классе
    // Каждый производный класс ОБЯЗАН переопределить эти методы

    // Площадь фигуры
    virtual double area() const = 0;

    // Периметр фигуры
    virtual double perimeter() const = 0;

    // Вывод информации о фигуре
    virtual void print() const = 0;

    // ===================================================
    // СТАТИЧЕСКИЙ МЕТОД ДЛЯ ПОЛУЧЕНИЯ КОЛИЧЕСТВА ФИГУР
    // ===================================================
    // static метод можно вызывать без создания объекта: Shape::getCount()
    // @return текущее количество существующих фигур
    static int getCount() {
        return shapeCount;
    }
};

// ===================================================
// ИНИЦИАЛИЗАЦИЯ СТАТИЧЕСКОГО ПОЛЯ
// ===================================================
// Статические поля классов должны быть определены (инициализированы) вне класса
// Начальное значение 0 - ещё ни одной фигуры не создано
int Shape::shapeCount = 0;

// ===================================================
// КЛАСС CIRCLE (КРУГ) - НАСЛЕДУЕТ ОТ SHAPE
// ===================================================
// Круг - это частный случай фигуры
class Circle : public Shape {
private:
    double radius;   // Радиус круга (должен быть положительным)

public:
    // ===================================================
    // КОНСТРУКТОР
    // ===================================================
    // @param r - радиус круга
    // @throws invalid_argument если радиус <= 0
    Circle(double r) : radius(r) {
        if (r <= 0) {
            throw invalid_argument("Radius must be positive");
        }
    }

    // ===================================================
    // ПЕРЕОПРЕДЕЛЕНИЕ ВИРТУАЛЬНЫХ МЕТОДОВ
    // ===================================================
    // override - ключевое слово, явно указывающее, что метод переопределяет виртуальный
    // Компилятор проверит, что такой метод действительно существует в базовом классе

    // Площадь круга: π * r²
    double area() const override {
        return 3.141592653589793 * radius * radius;
    }

    // Периметр круга (длина окружности): 2 * π * r
    double perimeter() const override {
        return 2 * 3.141592653589793 * radius;
    }

    // Вывод информации о круге
    void print() const override {
        cout << "Circle: radius = " << radius
            << ", area = " << area()
            << ", perimeter = " << perimeter() << endl;
    }
};

// ===================================================
// КЛАСС RECTANGLE (ПРЯМОУГОЛЬНИК) - НАСЛЕДУЕТ ОТ SHAPE
// ===================================================
class Rectangle : public Shape {
private:
    double width;   // Ширина прямоугольника
    double height;  // Высота прямоугольника

public:
    // ===================================================
    // КОНСТРУКТОР
    // ===================================================
    // @param w - ширина
    // @param h - высота
    Rectangle(double w, double h) : width(w), height(h) {
        if (w <= 0 || h <= 0) {
            throw invalid_argument("Sides must be positive");
        }
    }

    // Площадь прямоугольника: ширина * высота
    double area() const override {
        return width * height;
    }

    // Периметр прямоугольника: 2 * (ширина + высота)
    double perimeter() const override {
        return 2 * (width + height);
    }

    void print() const override {
        cout << "Rectangle: width = " << width
            << ", height = " << height
            << ", area = " << area()
            << ", perimeter = " << perimeter() << endl;
    }
};

// ===================================================
// КЛАСС TRIANGLE (ТРЕУГОЛЬНИК) - НАСЛЕДУЕТ ОТ SHAPE
// ===================================================
class Triangle : public Shape {
private:
    double side1, side2, side3;  // Три стороны треугольника

public:
    // ===================================================
    // КОНСТРУКТОР
    // ===================================================
    // @param s1, s2, s3 - длины сторон
    // @throws invalid_argument если стороны не образуют треугольник
    Triangle(double s1, double s2, double s3) : side1(s1), side2(s2), side3(s3) {
        // Проверка на положительность сторон
        if (s1 <= 0 || s2 <= 0 || s3 <= 0) {
            throw invalid_argument("Sides must be positive");
        }
        // Проверка неравенства треугольника
        // Треугольник существует только если сумма любых двух сторон БОЛЬШЕ третьей
        if (s1 + s2 <= s3 || s1 + s3 <= s2 || s2 + s3 <= s1) {
            throw invalid_argument("Triangle does not exist");
        }
    }

    // ===================================================
    // ПЛОЩАДЬ ПО ФОРМУЛЕ ГЕРОНА
    // ===================================================
    // Формула Герона: S = √(p(p-a)(p-b)(p-c))
    // где p - полупериметр (полусумма всех сторон)
    double area() const override {
        double p = perimeter() / 2;  // Полупериметр
        return sqrt(p * (p - side1) * (p - side2) * (p - side3));
    }

    // Периметр: сумма всех сторон
    double perimeter() const override {
        return side1 + side2 + side3;
    }

    void print() const override {
        cout << "Triangle: sides = " << side1 << ", " << side2 << ", " << side3
            << ", area = " << area()
            << ", perimeter = " << perimeter() << endl;
    }
};

