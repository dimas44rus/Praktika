#pragma once

// Класс "LightBulb" (Лампочка) - демонстрация ИНКАПСУЛЯЦИИ
// Инкапсуляция - это сокрытие внутреннего состояния объекта от прямого доступа извне
class LightBulb
{
private:
    // private: - эти поля доступны ТОЛЬКО внутри методов класса
    // Прямой доступ из main() или других функций ЗАПРЕЩЁН
    bool isOn;  // Состояние лампочки: true = включена, false = выключена

public:
    // public: - эти методы доступны из любой части программы
    // Это "интерфейс" класса - то, как внешний код взаимодействует с объектом

    // Мутаторы (модификаторы) - методы, которые ИЗМЕНЯЮТ состояние объекта
    void turnOn();   // Включить лампочку
    void turnOff();  // Выключить лампочку
    void toggle();   // Переключить состояние (если было включено - выключит, и наоборот)

    // Аксессор (инспектор) - метод, который ВОЗВРАЩАЕТ состояние объекта
    // Не изменяет объект, только читает данные
    const char* getState();  // Возвращает строку "ON" или "OFF"
};