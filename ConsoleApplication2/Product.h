// Класс "Product" (Товар) - демонстрирует простейший класс с публичными полями
class Product
{
public:
    // public: - эти поля доступны из любой части программы

    char name[50];    // Название товара (массив символов, максимум 50)
    int quantity;     // Количество товара на складе
    double price;     // Цена за одну единицу товара

    // Методы класса (функции, которые работают с данными класса)
    void print();           // Вывод информации о товаре
    double getTotalCost();  // Расчёт общей стоимости (quantity * price)
};