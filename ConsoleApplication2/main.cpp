    printSeparator("Товар на складе");

    // Создаём объект класса Product
    Product product;

    // Заполняем поля объекта
    // strcpy_s - безопасное копирование строк (указываем размер буфера 50)
    strcpy_s(product.name, 50, "Монитор");
    product.quantity = 15;
    product.price = 15000.0;

    // Вызываем метод print() - выводит информацию о товаре
    product.print();

    // Вызываем метод getTotalCost() - вычисляет общую стоимость
    // fixed и setprecision(2) - для вывода двух знаков после запятой
    cout << "\nОбщая стоимость: " << fixed << setprecision(2)
        << product.getTotalCost() << " руб." << endl;

