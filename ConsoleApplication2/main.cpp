    printSeparator("Банковский счёт");

    // Создаём банковский счёт (вызывается конструктор)
    // Начальный баланс: 10000 руб.
    BankAccount account("Петров П.П.", 789012, 10000);

    // Выводим информацию о счёте
    account.printInfo();

    // Пополняем счёт на 5000 руб.
    account.deposit(5000);
    cout << "Баланс после пополнения: " << fixed << setprecision(2)
        << account.getBalance() << " руб." << endl;

    // Снимаем 3000 руб.
    account.withdraw(3000);
    cout << "Баланс после снятия: " << fixed << setprecision(2)
        << account.getBalance() << " руб." << endl;

