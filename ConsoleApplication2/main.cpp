    Thermometer therm3("Улица", -10.0);

    // Выводим информацию о каждом термометре
    cout << "Термометр №1 (конструктор по умолчанию):" << endl;
    therm1.printInfo();

    cout << "\nТермометр №2 (только температура):" << endl;
    therm2.printInfo();

    cout << "\nТермометр №3 (место и температура):" << endl;
    therm3.printInfo();

