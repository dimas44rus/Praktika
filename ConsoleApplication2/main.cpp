    // ЗАДАЧА 2: Лампочка с выключателем (класс LightBulb)
    // Демонстрация: инкапсуляция (закрытое поле isOn, открытые методы)
    // ============================================================
    printSeparator("Лампочка с выключателем");

    // Создаём объект лампочки
    LightBulb bulb;

    // Включаем лампочку
    bulb.turnOn();
    cout << "Состояние: " << bulb.getState() << endl;

    // Переключаем состояние (включено -> выключено)
    bulb.toggle();
    cout << "Состояние: " << bulb.getState() << endl;

    // Снова переключаем (выключено -> включено)
    bulb.toggle();
    cout << "Состояние: " << bulb.getState() << endl;

    // Выключаем лампочку
    bulb.turnOff();
    cout << "Состояние: " << bulb.getState() << endl;

    // ============================================================
