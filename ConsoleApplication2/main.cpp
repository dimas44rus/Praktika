    // Демонстрация: перегрузка конструкторов (3 варианта)
    // ============================================================
    printSeparator("Термометр");

    // 1. Конструктор по умолчанию (без параметров)
    // Устанавливается температура 0°C и место "Неизвестно"
    Thermometer therm1;

    // 2. Конструктор с одним параметром (только температура)
