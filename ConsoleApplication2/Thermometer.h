#pragma once

// Класс "Thermometer" (Термометр) - демонстрация ПЕРЕГРУЗКИ КОНСТРУКТОРОВ
// Перегрузка - это несколько конструкторов (или методов) с одним именем,
// но разными параметрами. Компилятор выбирает нужный по количеству и типу аргументов.
class Thermometer
{
private:
    double celsius;   // Температура в градусах Цельсия
    char* location;   // Место измерения (динамическая строка)

public:
    // ПЕРЕГРУЖЕННЫЕ КОНСТРУКТОРЫ (3 варианта)

    // 1. Конструктор по умолчанию (без параметров)
    // Устанавливает температуру 0°C и место "Неизвестно"
    Thermometer();

    // 2. Конструктор с одним параметром (только температура)
    // Место будет "Неизвестно"
    Thermometer(double tempCelsius);

    // 3. Конструктор с двумя параметрами (место и температура)
    Thermometer(const char* place, double tempCelsius);

    // Деструктор - освобождает память
    ~Thermometer();

    // Геттер - возвращает температуру в Цельсиях
    double getCelsius();

    // Конвертация в Фаренгейты: F = C × 9/5 + 32
    double getFahrenheit();

    // Вывод информации о термометре
    void printInfo();
};