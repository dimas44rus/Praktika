#pragma once

// Шаблонный класс узла динамической структуры (Слайд 8)
// Используем template, чтобы стек мог работать с абсолютно любым типом данных T
template <typename T>
struct elem {
    T value;                 // Информационное поле (в нашей задаче тут будет лежать IntPair)
    elem<T>* next = nullptr; // Адресное поле: указатель на следующий элемент, находящийся «ниже» в стеке
};

// Функция добавления нового элемента на вершину стека (Слайд 9)
// Передаем указатель по ссылке (elem<T>*&), чтобы изменять оригинал вершины стека в main
template <typename T>
void push(elem<T>*& stack, T value) {
    elem<T>* newel = new elem<T>; // Выделяем динамическую память под новый узел
    newel->value = value;         // Записываем переданное значение в узел

    if (!stack) {                 // Если стек был пуст (указатель равен nullptr)
        stack = newel;            // Новый элемент сам становится вершиной стека
    }
    else {                      // Если в стеке уже были элементы
        newel->next = stack;      // Направляем указатель нового элемента на текущую вершину
        stack = newel;            // Передвигаем вершину стека на только что созданный элемент
    }
}

// Функция извлечения элемента с вершины стека (Слайд 9)
// Возвращает true, если элемент успешно извлечен, и false, если стек оказался пуст
template <typename T>
bool pop(elem<T>*& stack, T& value) {
    if (!stack) return false;     // Защита: если стек пуст, удалять нечего

    elem<T>* rem = stack;         // Запоминаем адрес текущей вершины во временный указатель, чтобы удалить его позже
    value = stack->value;         // Копируем значение из вершины в переменную-приемник
    stack = stack->next;          // Сдвигаем указатель вершины на следующий элемент (на уровень ниже)
    delete rem;                   // Освобождаем память, которую занимал удаленный элемент
    return true;                  // Сигнализируем об успешном удалении
}

// Функция просмотра значения на вершине стека БЕЗ его удаления (Слайд 10)
// Возвращает константный указатель на значение, чтобы случайно его не испортить
template <typename T>
const T* peek(const elem<T>* stack) {
    if (!stack) return nullptr;   // Если стек пуст, возвращать нечего
    return &stack->value;         // Возвращаем адрес информационного поля вершины
}

// Функция безопасного перехода к следующему элементу стека (Слайд 10)
// Используется для итерации (обхода) структуры в циклах
template <typename T>
elem<T>* next_elem(const elem<T>* el) {
    if (el) return el->next;      // Если текущий узел существует, возвращаем указатель на следующий за ним
    return nullptr;               // Если узел нулевой, возвращаем nullptr
}

// Функция полной очистки стека и освобождения памяти (Слайд 10)
template <typename T>
void clear(elem<T>*& stack) {
    while (stack) {               // Цикл работает, пока в стеке остается хотя бы один узел
        elem<T>* rem = stack;     // Запоминаем текущую вершину
        stack = stack->next;      // Шагаем к следующему элементу
        delete rem;               // Уничтожаем старую вершину
    }                             // По окончании цикла указатель stack гарантированно станет равным nullptr
}
