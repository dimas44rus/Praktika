#include "processor.h"

// Функция обхода стека и зануления элементов по условию суммы
void resetPairsBySum(elem<IntPair>* stack, int targetSum) {
    // Создаем рабочий указатель 'curr' и устанавливаем его на начало (вершину) стека.
    // Это позволяет нам двигаться по стеку, не теряя адрес самой вершины.
    elem<IntPair>* curr = stack;

    // Запускаем цикл обхода стека сверху вниз (аналогично примеру на Слайде 11)
    while (curr) {
        // Считаем сумму первого и второго числа в текущей паре структуры IntPair
        int currentSum = curr->value.first + curr->value.second;

        // Проверяем: совпадает ли полученная сумма с числом, которое ввел пользователь?
        if (currentSum == targetSum) {
            curr->value.first = 0;  // Если совпала, зануляем первое число пары
            curr->value.second = 0; // Зануляем второе число пары
        }

        // Переходим к следующему элементу стека с помощью функции next_elem (Слайд 11)
        curr = next_elem(curr);
    }
}
