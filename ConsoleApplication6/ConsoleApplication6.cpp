#include <iostream>  // Для ввода/вывода
#include <windows.h> // Для русского языка
using namespace std;

// ФУНКЦИЯ ДЛЯ ПРОВЕРКИ ВИСОКОСНОСТИ (та же, что в задании 3)
// Принимает: год (целое число)
// Возвращает: 1 (если високосный) или 0 (если нет)
int isLeapYear(int year) {
    // Проверяем условие: делится на 4, но не на 100, или делится на 400
    if ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0)) {
        return 1; // Да, високосный
    }
    else {
        return 0; // Нет, не високосный
    }
}

int main() { // Начало главной функции

    SetConsoleOutputCP(CP_UTF8);

    // SetConsoleCP - устанавливает кодировку для ВВОДА текста из консоли
    // CP_UTF8 - чтобы корректно читать русские буквы, введённые пользователем
    SetConsoleCP(CP_UTF8);

    // Объявляем переменные:
    int god, mes; // god - год, mes - месяц
    int dney;     // переменная, куда запишем количество дней в месяце

    // ПРОСИМ ПОЛЬЗОВАТЕЛЯ ВВЕСТИ ГОД:
    cout << "Введите год: ";
    cin >> god; // Сохраняем в переменную god

    // ПРОСИМ ПОЛЬЗОВАТЕЛЯ ВВЕСТИ МЕСЯЦ:
    cout << "Введите месяц (1-12): ";
    cin >> mes; // Сохраняем в переменную mes

    // ПРОВЕРЯЕМ, ПРАВИЛЬНЫЙ ЛИ ВВЕДЁН МЕСЯЦ:
    if (mes < 1 || mes > 12) { // Если месяц меньше 1 ИЛИ больше 12
        cout << "Ошибка: месяц должен быть от 1 до 12" << endl;
        return 1; // Завершаем программу с кодом ошибки 1
    }

    // ОПРЕДЕЛЯЕМ КОЛИЧЕСТВО ДНЕЙ В ЗАВИСИМОСТИ ОТ МЕСЯЦА:

    if (mes == 2) { // ЕСЛИ МЕСЯЦ = ФЕВРАЛЬ (номер 2)
        // Для февраля нужно проверить, високосный ли год
        if (isLeapYear(god) == 1) { // Вызываем нашу функцию. Если вернула 1
            dney = 29; // В високосном феврале 29 дней
            cout << "(Год " << god << " високосный)" << endl; // Сообщаем пользователю
        }
        else { // Если функция вернула 0 (год не високосный)
            dney = 28; // В обычном феврале 28 дней
            cout << "(Год " << god << " не високосный)" << endl;
        }
    }
    else if (mes == 4 || mes == 6 || mes == 9 || mes == 11) {
        // ЕСЛИ МЕСЯЦ: апрель(4), июнь(6), сентябрь(9) или ноябрь(11)
        // В этих месяцах всегда 30 дней
        dney = 30;
    }
    else {
        // ВСЕ ОСТАЛЬНЫЕ МЕСЯЦЫ (1,3,5,7,8,10,12)
        // В них всегда 31 день
        dney = 31;
    }

    // ВЫВОДИМ РЕЗУЛЬТАТ НА ЭКРАН:
    cout << "В месяце " << mes << " года " << god << " - " << dney << " дней" << endl;

    return 0; // Успешное завершение программы
}